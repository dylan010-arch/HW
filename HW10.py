A = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]

def multiply_by_2(A):
    result = []
    for row in A:
        new_row = [x * 2 for x in row]
        result.append(new_row)
    return result

result_A = multiply_by_2(A)

print("Исходная матрица:")
for row in A:
    print(row)

print("Матрица после умножения каждого элемента на 2:")
for row in result_A:
    print(row)

