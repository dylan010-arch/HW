students = {
    "Анна": [85, 90, 78, 92, 88],
    "Арман": [76, 89, 91, 82, 95],
    "Даниел": [90, 88, 87, 94, 79],
}

def средняя_оценка(оценки):
    if len(оценки) == 0:
        return 0 
    сумма_оценок = sum(оценки)
    средняя = сумма_оценок / len(оценки)
    return средняя

for студент, оценки in students.items():
    средняя = средняя_оценка(оценки)
    print(f"{студент}: Средняя оценка = {средняя}")

