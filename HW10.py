coordinates= (3,4,5)
def sum_of_coordinates(coordinates):
    if len(coordinates) == 3: 
        x, y, z = coordinates
        return x + y + z
    else:
        return None  
    
result = sum_of_coordinates(coordinates)
if result is not None:
    print(f"Сумма координат точки {coordinates} равна {result}")
else:
    print("Неправильный формат кортежа координат.")
