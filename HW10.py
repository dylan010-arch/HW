фильмы_по_жанрам = {
    'комедия' : ['Мачо и ботан', 'Тупой и еще тупее ', '1+1'],
    'драма': ['Джейн Остин', '12 лет рабства', 'Прислуга'],
    'экшн': ['Крепкий орешек', 'Индиана Джонс', 'Никто']
}

def добавить_фильм(жанр, название_фильма):
    if жанр in фильмы_по_жанрам:
        фильмы_по_жанрам[жанр].append(название_фильма)
        print(f"Фильм {название_фильма} добавлен в жанр {жанр}")
    else:
        print(f"Жанр {жанр} не найден в каталоге фильмов.")

жанр = input("Введите жанр фильма: ").lower()
название_фильма = input("Введите название фильма:")
добавить_фильм(жанр, название_фильма)

print(фильмы_по_жанрам)

